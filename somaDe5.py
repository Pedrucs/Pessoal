#Somando números digitados pelo Usuário
Numero = int(input("Qual o primeiro número? "))
Numero2 = int(input("Qual o segundo número? "))
Numero3 = int(input("Qual o terceiro número? "))
Numero4 = int(input("Qual o quarto número? "))
Numero5 = int(input("Qual o quinto número? "))
Soma = Numero + Numero2 + Numero3 + Numero4 + Numero5
Multiplicação = Numero * Numero2 * Numero3 * Numero4 * Numero5

print("O resultado da soma foi: ", Soma)

#Somando os números
print('Os números digitados foram: ', Numero, Numero2, Numero3, Numero4, Numero5)

#Armazenando os valores em vetor

print("Seus numeros são: ", [Numero, Numero2, Numero3, Numero4, Numero5])

#Multiplicando os numeros 
print("O resultado da multiplicação foi: ", Multiplicação)



